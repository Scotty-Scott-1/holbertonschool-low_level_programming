#include <stdio.h>
#include "3-calc.h"
/**
* op_add - a function
* @a: an int
* @b: an int
* Description: adds a and b
* Return: a+b
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
* op_sub - a function
* @a: an int
* @b: an int
* Description: subtracts b from a
* Return: a-b
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
* op_mul - a function
* @a: an int
* @b: an int
* Description: multiplies a by b
* Return: a*b
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
* op_div - a function
* @a: an int
* @b: an int
* Description: divides a by b
* Return: a/b
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
* op_mod - a function
* @a: an int
* @b: an int
* Description: remainder of a by b
* Return: a%b
*/
int op_mod(int a, int b)
{
	return (a % b);
}

