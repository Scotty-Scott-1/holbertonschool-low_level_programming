#include <stdio.h>
/**
 * main - Prints all single digit numbers of base 10 starting at 0
 *
 * Description - A function that prints numbers
 *
 * Return: 0 success
 */
int main(void)
{
	int i = '0';
	char n = '\n';

	for (i = '0'; i <= '9'; i++)
	{
	putchar (i);
	}
	putchar (n);
	return (0);








}
